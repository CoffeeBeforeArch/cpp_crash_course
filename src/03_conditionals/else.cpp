#include <iostream>

int main() {
  // Declare and initialize variable
  int a = 10;
  
  // The if-side code executes if a is 10
  if (a == 10) {
    std::cout << "Inside if statement!\n";
  // The else-side code executes in all other cases
  } else {
    std::cout << "Inside else statement!\n";
  }

  return 0;
}
