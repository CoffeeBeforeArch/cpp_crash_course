#include <iostream>

int main() {
  // Declare variables
  float a;
  float b;

  // Initialize variables
  a = 5.823f;
  b = 10.123f;

  // Compute the sum
  float sum = a + b;

  // Print the result
  std::cout << "The sum of " << a << " and " << b << " is " << sum << '\n';

  return 0;
}
